int32_t i32CosLookup(int16_t angle_x_10)
{
    int16_t ix, sin_ix, cos_ix;
    int16_t angle;

    if (C_FALSE == tLookupTableIsInitialized)
       vCopySinCosTableROM2RAM();
    
    angle = angle_x_10 % 3600;

    if (angle < 0)
    {
        angle += 3600;
    }

    ix = angle % 900;

    sin_ix = ix;
    cos_ix = (900 - ix);

    if( (angle >= 0 && angle < 900) || angle == 3600 ) 
    {
        return i32SinCosLookup[cos_ix];	
    }
    else if( angle >= 900 && angle < 1800 )
    {
        return (-i32SinCosLookup[sin_ix]);
    }
    else if( angle >= 1800 && angle < 2700 ) 
    {
       return (-i32SinCosLookup[cos_ix]);
    }
    else 
    {
        return i32SinCosLookup[sin_ix];		
    }
}

/****************************************************************************/

void vSinCosLookUp(int16_t angle_x_10, int32_t *sin_, int32_t *cos_)
{
    int16_t ix, sin_ix, cos_ix;
    int32_t val_sin, val_cos;
    int16_t angle;

    if ((C_NULL != sin_) && (C_NULL != cos_))
    {
        if (C_FALSE == tLookupTableIsInitialized)
            vCopySinCosTableROM2RAM();
        
        angle = angle_x_10 % 3600;
        
        if (angle < 0)
        {
            angle += 3600;
        }
        
        ix = angle % 900;
        
        sin_ix = ix;
        cos_ix = (900 - ix);
        
        if( (angle >= 0 && angle < 900) || angle == 3600 ) 
        {
            val_sin = i32SinCosLookup[sin_ix];
            val_cos = i32SinCosLookup[cos_ix];	
        }
        else if( angle >= 900 && angle < 1800 )
        {
            val_sin = i32SinCosLookup[cos_ix];
            val_cos = -i32SinCosLookup[sin_ix];
        }
        else if( angle >= 1800 && angle < 2700 ) 
        {
           val_sin = -i32SinCosLookup[sin_ix];
           val_cos = -i32SinCosLookup[cos_ix];
        }
        else 
        {
            val_sin = -i32SinCosLookup[cos_ix];
            val_cos = i32SinCosLookup[sin_ix];		
        }
        
        *sin_ = val_sin;
        *cos_ = val_cos; 
    }
}

