void vCopySinCosTableROM2RAM(void)
{
    asm_copyfunction((uint32_t)&angl_sin_cos[0], (uint32_t)&angl_sin_cos[900], (uint32_t)&i32SinCosLookup[0]);
    tLookupTableIsInitialized = C_TRUE;
}

/****************************************************************************/
    
int32_t i32SinLookup(int16_t angle_x_10)
{
    int16_t ix, sin_ix, cos_ix;
    int16_t angle;

    if (C_FALSE == tLookupTableIsInitialized)
        vCopySinCosTableROM2RAM();
	
    angle = angle_x_10 % 3600;

    if (angle < 0)
    {
        angle += 3600;
    }

    ix = angle % 900;

    sin_ix = ix;
    cos_ix = (900 - ix);

    if( (angle >= 0 && angle < 900) || angle == 3600 ) 
    {
        return i32SinCosLookup[sin_ix];
    }
    else if( angle >= 900 && angle < 1800 )
    {
        return i32SinCosLookup[cos_ix];
    }
    else if( angle >= 1800 && angle < 2700 ) 
    {
        return (-i32SinCosLookup[sin_ix]);
    }
    else 
    {
        return (-i32SinCosLookup[cos_ix]);
    }
}

